const logout = async () => {
    const response = await fetch('/logout', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
    });
  
    if (response.ok) {
      document.location.replace('/');
    } else {
      alert('Problem logging out.');
    }
  };
  
  document.querySelector('.logout-btn').addEventListener('click', logout);
  